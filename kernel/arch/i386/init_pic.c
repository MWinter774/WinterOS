#include <kernel/init_pic.h>
#include <kernel/assembly_wrapper.h>

void kernel_init_pic()
{
  __asm__("cli");
  outb(0x20, 0x11);
	outb(0xA0, 0x11);
	outb(0x21, 0x20);
	outb(0xA1, 0x28);
	outb(0x21, 0x04);
	outb(0xA1, 0x02);
	outb(0x21, 0x01);
	outb(0xA1, 0x01);
	outb(0x21, 0x0);
	outb(0xA1, 0x0);
  __asm__("sti");
}
